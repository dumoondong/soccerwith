package com.example.bootweb01;

public class RecordTO {
	private String seq;
	private String season;
	private String leaguewin;
	private String leaguecup;
	private String list;
	
	
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getSeason() {
		return season;
	}
	public void setSeason(String season) {
		this.season = season;
	}
	public String getLeaguewin() {
		return leaguewin;
	}
	public void setLeaguewin(String leaguewin) {
		this.leaguewin = leaguewin;
	}
	public String getLeaguecup() {
		return leaguecup;
	}
	public void setLeaguecup(String leaguecup) {
		this.leaguecup = leaguecup;
	}
	public String getList() {
		return list;
	}
	public void setList(String list) {
		this.list = list;
	}
}
